DROP SCHEMA IF EXISTS PASSENGERSCREENING;
CREATE DATABASE PASSENGERSCREENING;
USE PASSENGERSCREENING;

CREATE TABLE IMAGE (
    ID INT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(40) NOT NULL,
    MD5 VARCHAR(100) UNIQUE,
    P1 FLOAT(24),
    P2 FLOAT(24),
    P3 FLOAT(24),
    P4 FLOAT(24),
    P5 FLOAT(24),
    P6 FLOAT(24),
    P7 FLOAT(24),
    P8 FLOAT(24),
    P9 FLOAT(24),
    P10 FLOAT(24),
    P11 FLOAT(24),
    P12 FLOAT(24),
    P13 FLOAT(24),
    P14 FLOAT(24),
    P15 FLOAT(24),
    P16 FLOAT(24),
    P17 FLOAT(24),
    PRIMARY KEY (ID)
);